SELECT 
    {{ safe_cast_to_number('Id') }} AS date_id,
    CAST(Date AS DATE) AS date,
    CAST(DateLongDescription AS VARCHAR(255)) AS date_long_description,
    CAST(DateShortDescription AS VARCHAR(50)) AS date_short_description,
    CAST(DayLongName AS VARCHAR(50)) AS day_long_name,
    CAST(DayShortName AS VARCHAR(10)) AS day_short_name,
    CAST(MonthLongName AS VARCHAR(50)) AS month_long_name,
    CAST(MonthShortName AS VARCHAR(10)) AS month_short_name,
    {{ safe_cast_to_number('CalendarDay') }} AS calendar_day,
    {{ safe_cast_to_number('CalendarWeek') }} AS calendar_week,
    {{ safe_cast_to_number('CalendarWeekStartDateId') }} AS calendar_week_start_date_id,
    {{ safe_cast_to_number('CalendarWeekEndDateId') }} AS calendar_week_end_date_id,
    CAST(CalendarWeekStartDate AS DATE) AS calendar_week_start_date,
    CAST(CalendarWeekEndDate AS DATE) AS calendar_week_end_date,
    {{ safe_cast_to_number('CalendarDayInWeek') }} AS calendar_day_in_week,
    {{ safe_cast_to_number('CalendarMonth') }} AS calendar_month,
    {{ safe_cast_to_number('CalendarMonthStartDateId') }} AS calendar_month_start_date_id,
    {{ safe_cast_to_number('CalendarMonthEndDateId') }} AS calendar_month_end_date_id,
    CAST(CalendarMonthStartDate AS DATE) AS calendar_month_start_date,
    CAST(CalendarMonthEndDate AS DATE) AS calendar_month_end_date,
    {{ safe_cast_to_number('CalendarNumberOfDaysInMonth') }} AS calendar_number_of_days_in_month,
    {{ safe_cast_to_number('CalendarDayInMonth') }} AS calendar_day_in_month,
    {{ safe_cast_to_number('CalendarQuarter') }} AS calendar_quarter,
    {{ safe_cast_to_number('CalendarQuarterStartDateId') }} AS calendar_quarter_start_date_id,
    {{ safe_cast_to_number('CalendarQuarterEndDateId') }} AS calendar_quarter_end_date_id,
    CAST(CalendarQuarterStartDate AS DATE) AS calendar_quarter_start_date,
    CAST(CalendarQuarterEndDate AS DATE) AS calendar_quarter_end_date,
    {{ safe_cast_to_number('CalendarNumberOfDaysInQuarter') }} AS calendar_number_of_days_in_quarter,
    {{ safe_cast_to_number('CalendarDayInQuarter') }} AS calendar_day_in_quarter,
    {{ safe_cast_to_number('CalendarYear') }} AS calendar_year,
    {{ safe_cast_to_number('CalendarYearStartDateId') }} AS calendar_year_start_date_id,
    {{ safe_cast_to_number('CalendarYearEndDateId') }} AS calendar_year_end_date_id,
    CAST(CalendarYearStartDate AS DATE) AS calendar_year_start_date,
    CAST(CalendarYearEndDate AS DATE) AS calendar_year_end_date,
    {{ safe_cast_to_number('CalendarNumberOfDaysInYear') }} AS calendar_number_of_days_in_year
FROM {{ ref('calendar') }}
WHERE Id IS NOT NULL
ORDER BY id